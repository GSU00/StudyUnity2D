using UnityEngine;

public class Study_Operator : MonoBehaviour
{
    public int currentLevel = 10;
    public int maxLevel = 99;

    void Start()
    {
        string msg = currentLevel >= maxLevel ? "현재 만렙입니다." : "현재 만렙이 아닙니다.";

        Debug.Log(msg);

        if (currentLevel >= maxLevel)
        {
            Debug.Log($"현재 레벨은 만렙입니다.");
        }
        else
        {
            Debug.Log($"현재 레벨은 만렙이 아닙니다.");
        }
    }
}
